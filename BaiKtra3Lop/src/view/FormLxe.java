    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        // TODO add your handling code here:
        if(them){
            String ten, diachi;
            try{
                ten = jTextField2.getText();
                diachi = jTextField3.getText();
                if(ten.isEmpty() || diachi.isEmpty()) throw new TrongException();
                int ma = Integer.parseInt(jTextField1.getText());
                laiXe lx = new laiXe(ma, ten, diachi, jComboBox1.getSelectedItem().toString());
                tm.addRow(lx.toObject());
                lLaiXe.add(lx);
                buttonState(true);
                them = false;
            } catch (TrongException e){
                JOptionPane.showMessageDialog(this, "Khong de trong");
            }
        }
        
        if(sua){
            int row = jTable1.getSelectedRow();
            String ten, diachi;
            ten = jTextField2.getText();
            diachi = jTextField3.getText();
            int ma = Integer.parseInt(jTextField1.getText());
            String trinhdo = jComboBox1.getSelectedItem().toString();
            laiXe lx = new laiXe(ma, ten, diachi, trinhdo);
            tm.setValueAt(ma, row, 0);
            tm.setValueAt(ten, row, 1);
            tm.setValueAt(diachi, row, 2);
            tm.setValueAt(trinhdo, row, 3);
            lLaiXe.set(row, lx);
            sua = false;
            buttonState(true);
        }
    }//GEN-LAST:event_btCapNhatActionPerformed

