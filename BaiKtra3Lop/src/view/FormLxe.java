    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        buttonState(false);
        them = true;
        if(lLaiXe != null && lLaiXe.size() > 0){
            laiXe.setSma(lLaiXe.get(lLaiXe.size() - 1).getMa() + 1);
        } else {
            lLaiXe = new ArrayList<>();
        }
        jTextField1.setText(laiXe.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_btThemActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if(row < 0 || row > jTable1.getRowCount() - 1){
            JOptionPane.showMessageDialog(this, "Chon lai xe de xoa!");
        } else {
            tm.removeRow(row);
            lLaiXe.remove(row);
        }
    }//GEN-LAST:event_btXoaActionPerformed

